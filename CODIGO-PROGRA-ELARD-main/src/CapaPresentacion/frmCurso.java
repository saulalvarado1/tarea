    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
    
        clsECurso objEC = new clsECurso();
        clsNCurso objNC = new clsNCurso();
        
        objEC.setCodigo(txtBCodigo.getText());
        ResultSet rs = objNC.mtdBuscarCurso(objEC);

        try {
            if (rs.next()) {
                txtCodigo.setText(rs.getString("codigo"));
                txtNombre.setText(rs.getString("nombre"));
                txtCreditos.setText(String.valueOf(rs.getInt("creditos")));
                txtPrerequisito.setText(rs.getString("prerequisito"));
            } else {
                JOptionPane.showMessageDialog(null, "No se encontró el curso");
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void tblCursoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCursoMouseClicked
        
        int fila = tblCurso.getSelectedRow();
        if (fila != -1) {
            txtCodigo.setText(tblCurso.getValueAt(fila, 0).toString());
            txtNombre.setText(tblCurso.getValueAt(fila, 1).toString());
            txtCreditos.setText(tblCurso.getValueAt(fila, 2).toString());
            txtPrerequisito.setText(tblCurso.getValueAt(fila, 3).toString());
        }
        
    }//GEN-LAST:event_tblCursoMouseClicked

