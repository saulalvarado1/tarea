    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        
        clsEClase objECl = new clsEClase();
        clsNClase objNCl = new clsNClase();
        
        String idBuscar = txtBCodigo.getText(); // O el campo que uses para buscar

        if (idBuscar.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese un ID para buscar");
            return;
        }

        objECl.setIdclase(idBuscar);
        ResultSet rs = objNCl.mtdBuscarClase(objECl);

        try {
            if (rs.next()) {
                txtIdClase.setText(rs.getString("idclase"));

                // Los códigos FK que vienen de la base de datos
                String dniVal = rs.getString("dniemp");
                String cursoVal = rs.getString("codcurso");
                String docVal = rs.getString("coddoc");

                // Seleccionar en los combos por ID (no por texto)
                mtdSeleccionarComboPorId(cmbEmpleado, dniVal);
                mtdSeleccionarComboPorId(cmbCurso, cursoVal);
                mtdSeleccionarComboPorId(cmbDocente, docVal);

            } else {
                JOptionPane.showMessageDialog(null, "No se encontró ningún registro con ese ID");
            }
        } catch (SQLException e) {
            System.out.println("Error en búsqueda: " + e.getMessage());
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

    // Método auxiliar necesario para el buscador (Selecciona por el ID oculto del clsItem)
    private void mtdSeleccionarComboPorId(JComboBox combo, String id) {
        for (int i = 0; i < combo.getItemCount(); i++) {
            clsItem item = (clsItem) combo.getItemAt(i);
            if (item.getId().equals(id)) {
                combo.setSelectedIndex(i);
                break;
            }
        }
    }
    
    private void tblClaseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClaseMouseClicked

        int fila = tblClase.getSelectedRow();
        if (fila != -1) {
            txtIdClase.setText(tblClase.getValueAt(fila, 0).toString());

            // Función para seleccionar por texto en combos
            mtdSeleccionarCombo(cmbEmpleado, tblClase.getValueAt(fila, 1).toString());
            mtdSeleccionarCombo(cmbCurso, tblClase.getValueAt(fila, 2).toString());
            mtdSeleccionarCombo(cmbDocente, tblClase.getValueAt(fila, 3).toString());
        }

    }//GEN-LAST:event_tblClaseMouseClicked

    private void mtdSeleccionarCombo(JComboBox combo, String valor) {
        for (int i = 0; i < combo.getItemCount(); i++) {
            if (combo.getItemAt(i).toString().equals(valor)) {
                combo.setSelectedIndex(i);
                break;
            }
        }
    }
    
