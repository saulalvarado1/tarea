    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        
        clsEDetalleClase objEDC = new clsEDetalleClase();
        clsNDetalleClase objNDC = new clsNDetalleClase();
        
        clsItem clase = (clsItem) cmbClase.getSelectedItem();
        clsItem alumno = (clsItem) cmbAlumno.getSelectedItem();

        objEDC.setIdclase(clase.getId());
        objEDC.setCodalumno(alumno.getId());

        if (objNDC.mtdAgregarDetalleClase(objEDC)) {
            JOptionPane.showMessageDialog(null, "Alumno asignado a la clase");
            mtdListar(clase.getId());
        } else {
            JOptionPane.showMessageDialog(null, "Error: El alumno ya está en esta clase");
        }
        
    }//GEN-LAST:event_btnAgregarActionPerformed

