    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        
        String idABuscar = txtBCodigo.getText(); // O el campo de búsqueda que tengas

        if (idABuscar.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese un ID de matrícula");
            return;
        }

        // Buscamos en el combo si existe ese ID
        boolean encontrado = false;
        for (int i = 0; i < cmbMatricula.getItemCount(); i++) {
            clsItem item = (clsItem) cmbMatricula.getItemAt(i);
            if (item.getId().equals(idABuscar)) {
                cmbMatricula.setSelectedIndex(i);
                encontrado = true;
                break;
            }
        }

        if (encontrado) {
            // Al seleccionar el combo, el evento ActionPerformed debería disparar el mtdListar
            mtdListar(Integer.parseInt(idABuscar));
        } else {
            JOptionPane.showMessageDialog(null, "La matrícula no existe");
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void tblDetalleMatriculaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDetalleMatriculaMouseClicked

        int filaSeleccionada = tblDetalleMatricula.getSelectedRow();
        if (filaSeleccionada != -1) {
            // Obtenemos el ID de matrícula y el Curso de la tabla
            String idMat = tblDetalleMatricula.getValueAt(filaSeleccionada, 0).toString();
            String cursoNom = tblDetalleMatricula.getValueAt(filaSeleccionada, 1).toString();

            // Sincronizar Combos
            mtdSeleccionarCombo(cmbMatricula, idMat);
            mtdSeleccionarCombo(cmbCurso, cursoNom);
        }

    }//GEN-LAST:event_tblDetalleMatriculaMouseClicked

    private void mtdSeleccionarCombo(javax.swing.JComboBox combo, String valor) {
        for (int i = 0; i < combo.getItemCount(); i++) {
            if (combo.getItemAt(i).toString().equals(valor)) {
                combo.setSelectedIndex(i);
                break;
            }
        }
    }
    
