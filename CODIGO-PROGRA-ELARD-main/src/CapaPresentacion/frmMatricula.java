    private void mtdCargarCombos() {
        
        clsNEmpleado objNE = new clsNEmpleado();
        clsNAlumno objNA = new clsNAlumno();
        
        try {
            // Cargar Empleados
            ResultSet rsE = objNE.mtdListarEmpleado();
            cmbEmpleado.removeAllItems();
            while (rsE.next()) {
                cmbEmpleado.addItem(new clsItem(rsE.getString("dni"), rsE.getString("nombre")));
            }

            // Cargar Alumnos
            ResultSet rsA = objNA.mtdListarAlumno();
            cmbAlumno.removeAllItems();
            while (rsA.next()) {
                cmbAlumno.addItem(new clsItem(rsA.getString("codigo"), rsA.getString("nombre")));
            }
        } catch (Exception e) {
            System.out.println("Error: " + e.getMessage());
        }
    }
    
    public void mtdListar() {
        
        clsNMatricula objNM = new clsNMatricula();
        
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("Fecha");
        modelo.addColumn("Estado");
        modelo.addColumn("Empleado");
        modelo.addColumn("Alumno");

        tblMatricula.setModel(modelo);
        ResultSet rs = objNM.mtdListarMatricula();
        Object[] fila = new Object[5];

        try {
            while (rs.next()) {
                fila[0] = rs.getInt("idmatricula");
                fila[1] = rs.getInt("fecha");
                fila[2] = rs.getInt("estado");
                fila[3] = rs.getString("nombre_empleado");
                fila[4] = rs.getString("nombre_alumno");
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

