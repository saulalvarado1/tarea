    private void mtdCargarCombos() {
        try {
            // 1. Cargar Empleados
            clsNEmpleado objNE = new clsNEmpleado();
            ResultSet rsE = objNE.mtdListarEmpleado();
            cmbEmpleado.removeAllItems();
            while (rsE.next()) {
                cmbEmpleado.addItem(new clsItem(rsE.getString("dni"), rsE.getString("nombre")));
            }

            // 2. Cargar Cursos
            clsNCurso objNCu = new clsNCurso();
            ResultSet rsCu = objNCu.mtdListarCurso();
            cmbCurso.removeAllItems();
            while (rsCu.next()) {
                cmbCurso.addItem(new clsItem(rsCu.getString("codigo"), rsCu.getString("nombre")));
            }

            // 3. Cargar Docentes
            clsNDocente objNDo = new clsNDocente();
            ResultSet rsDo = objNDo.mtdListarDocente();
            cmbDocente.removeAllItems();
            while (rsDo.next()) {
                cmbDocente.addItem(new clsItem(rsDo.getString("codigo"), rsDo.getString("nombre")));
            }
        } catch (Exception e) {
            System.out.println("Error cargando combos: " + e.getMessage());
        }
    }
    
    public void mtdListar() {
        
        clsNClase objNCl = new clsNClase();
        
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID Clase");
        modelo.addColumn("Empleado");
        modelo.addColumn("Curso");
        modelo.addColumn("Docente");

        tblClase.setModel(modelo);
        ResultSet rs = objNCl.mtdListarClase();
        Object[] fila = new Object[4];

        try {
            while (rs.next()) {
                fila[0] = rs.getString("idclase");
                fila[1] = rs.getString("empleado"); // Viene del JOIN
                fila[2] = rs.getString("curso");    // Viene del JOIN
                fila[3] = rs.getString("docente");  // Viene del JOIN
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

