    private void mtdCargarCombos() {
        
        clsNClase objNCl = new clsNClase();
        clsNAlumno objNAl = new clsNAlumno();
        
        try {
            // Cargar Clases
            ResultSet rsC = objNCl.mtdListarClase();
            cmbClase.removeAllItems();
            while (rsC.next()) {
                cmbClase.addItem(new clsItem(rsC.getString("idclase"), rsC.getString("idclase")));
            }

            // Cargar Alumnos
            ResultSet rsA = objNAl.mtdListarAlumno();
            cmbAlumno.removeAllItems();
            while (rsA.next()) {
                cmbAlumno.addItem(new clsItem(rsA.getString("codigo"), rsA.getString("nombre")));
            }
        } catch (Exception e) {
            System.out.println("Error combos: " + e.getMessage());
        }
    }

    public void mtdListar(String idClase) {
        
        clsEDetalleClase objEDC = new clsEDetalleClase();
        clsNDetalleClase objNDC = new clsNDetalleClase();
        
        modelo = new DefaultTableModel();
        modelo.addColumn("ID CLASE");
        modelo.addColumn("COD ALUMNO");
        modelo.addColumn("NOMBRE ALUMNO");
        tblDetalleClase.setModel(modelo);

        objEDC.setIdclase(idClase);
        ResultSet rs = objNDC.mtdListarDetalleClase(objEDC);
        Object[] fila = new Object[3];

        try {
            while (rs.next()) {
                fila[0] = idClase;
                fila[1] = rs.getString("codalumno");
                fila[2] = rs.getString("nombre");
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

