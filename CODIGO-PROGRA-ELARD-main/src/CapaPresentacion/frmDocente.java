    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        
        clsEDocente objED = new clsEDocente();
        clsNDocente objND = new clsNDocente();
        
        objED.setCodigo(txtBCodigo.getText());
        ResultSet rs = objND.mtdBuscarDocente(objED);

        try {
            if (rs.next()) {
                txtCodigo.setText(rs.getString("codigo"));
                txtNombre.setText(rs.getString("nombre"));
            } else {
                JOptionPane.showMessageDialog(null, "No se encontró el docente");
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void tblDocenteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDocenteMouseClicked
        
        int fila = tblDocente.getSelectedRow();
        if (fila != -1) {
            txtCodigo.setText(tblDocente.getValueAt(fila, 0).toString());
            txtNombre.setText(tblDocente.getValueAt(fila, 1).toString());
        }
        
    }//GEN-LAST:event_tblDocenteMouseClicked

