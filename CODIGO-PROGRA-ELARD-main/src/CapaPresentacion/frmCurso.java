    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        clsECurso objEC = new clsECurso();
        clsNCurso objNC = new clsNCurso();
        
        objEC.setCodigo(txtCodigo.getText());
        int resp = JOptionPane.showConfirmDialog(null, "¿Seguro que desea eliminar este curso?");

        if (resp == JOptionPane.YES_OPTION) {
            if (objNC.mtdEliminarCurso(objEC)) {
                mtdListar();
                mtdLimpiar();
            }
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        
        clsECurso objEC = new clsECurso();
        clsNCurso objNC = new clsNCurso();
        
        objEC.setCodigo(txtCodigo.getText());
        objEC.setNombre(txtNombre.getText());
        objEC.setCreditos(Integer.parseInt(txtCreditos.getText()));
        objEC.setPrerequisito(txtPrerequisito.getText());

        if (objNC.mtdModificarCurso(objEC)) {
            JOptionPane.showMessageDialog(null, "Curso actualizado");
            mtdListar();
            mtdLimpiar();
        }
        
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

        clsECurso objEC = new clsECurso();
        clsNCurso objNC = new clsNCurso();

        objEC.setCodigo(txtCodigo.getText());
        objEC.setNombre(txtNombre.getText());
        // Convertimos el texto de créditos a entero
        objEC.setCreditos(Integer.parseInt(txtCreditos.getText()));
        objEC.setPrerequisito(txtPrerequisito.getText());

        if (objNC.mtdAgregarCurso(objEC)) {
            JOptionPane.showMessageDialog(null, "Curso registrado con éxito");
            mtdListar();
            mtdLimpiar();
        }
        
    }//GEN-LAST:event_btnAgregarActionPerformed

