    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        
        clsEMatricula objEM = new clsEMatricula();
        clsNMatricula objNM = new clsNMatricula();
        
        try {
            // 1. Validar que el campo de búsqueda no esté vacío
            if (txtBCodigo.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Ingrese un ID de Matrícula para buscar");
                return;
            }

            // 2. Pasar el ID (int) a la entidad
            objEM.setIdmatricula(Integer.parseInt(txtBCodigo.getText()));

            // 3. Llamar al método de la Capa de Negocio
            ResultSet rs = objNM.mtdBuscarMatricula(objEM);

            if (rs.next()) {
                // 4. Llenar los campos de texto
                txtIdMatricula.setText(String.valueOf(rs.getInt("idmatricula")));
                txtFecha.setText(String.valueOf(rs.getInt("fecha")));
                txtEstado.setText(String.valueOf(rs.getInt("estado")));

                // 5. Recuperar los IDs (FK) de la base de datos
                String dniVal = rs.getString("dniemp");
                String aluVal = rs.getString("codalumno");

                // 6. Posicionar los ComboBoxes usando el ID oculto
                mtdSeleccionarComboPorId(cmbEmpleado, dniVal);
                mtdSeleccionarComboPorId(cmbAlumno, aluVal);

            } else {
                JOptionPane.showMessageDialog(null, "No se encontró la matrícula con el ID especificado");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "El ID de búsqueda debe ser un número entero");
        } catch (SQLException e) {
            System.out.println("Error en búsqueda: " + e.getMessage());
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void mtdSeleccionarComboPorId(JComboBox combo, String id) {
        for (int i = 0; i < combo.getItemCount(); i++) {
            // Convertimos el ítem actual a nuestro objeto clsItem
            clsItem item = (clsItem) combo.getItemAt(i);

            // Comparamos el ID del objeto con el ID que traemos de la BD
            if (item.getId().equals(id)) {
                combo.setSelectedIndex(i);
                break;
            }
        }
    }
    
    private void tblMatriculaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMatriculaMouseClicked
        int fila = tblMatricula.getSelectedRow();
        if (fila != -1) {
            txtIdMatricula.setText(tblMatricula.getValueAt(fila, 0).toString());
            txtFecha.setText(tblMatricula.getValueAt(fila, 1).toString());
            txtEstado.setText(tblMatricula.getValueAt(fila, 2).toString());

            // Seleccionar en combos por nombre (Texto visible en la tabla)
            mtdSeleccionarCombo(cmbEmpleado, tblMatricula.getValueAt(fila, 3).toString());
            mtdSeleccionarCombo(cmbAlumno, tblMatricula.getValueAt(fila, 4).toString());
        }
    }//GEN-LAST:event_tblMatriculaMouseClicked

    private void mtdSeleccionarCombo(JComboBox combo, String valor) {
        for (int i = 0; i < combo.getItemCount(); i++) {
            if (combo.getItemAt(i).toString().equals(valor)) {
                combo.setSelectedIndex(i);
                break;
            }
        }
    }
    
