    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        
        String buscar = txtBCodigo.getText();
        boolean encontrado = false;
        for (int i = 0; i < cmbClase.getItemCount(); i++) {
            if (cmbClase.getItemAt(i).toString().equals(buscar)) {
                cmbClase.setSelectedIndex(i);
                encontrado = true;
                break;
            }
        }
        if (!encontrado)
            JOptionPane.showMessageDialog(null, "Clase no encontrada");
        
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void tblDetalleClaseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDetalleClaseMouseClicked

        int filaSeleccionada = tblDetalleClase.getSelectedRow();
        if (filaSeleccionada != -1) {
            // Obtenemos el ID de matrícula y el Curso de la tabla
            String idMat = tblDetalleClase.getValueAt(filaSeleccionada, 0).toString();
            String cursoNom = tblDetalleClase.getValueAt(filaSeleccionada, 1).toString();

            // Sincronizar Combos
            mtdSeleccionarCombo(cmbClase, idMat);
            mtdSeleccionarCombo(cmbAlumno, cursoNom);
        }

    }//GEN-LAST:event_tblDetalleClaseMouseClicked

    private void mtdSeleccionarCombo(javax.swing.JComboBox combo, String valor) {
        for (int i = 0; i < combo.getItemCount(); i++) {
            if (combo.getItemAt(i).toString().equals(valor)) {
                combo.setSelectedIndex(i);
                break;
            }
        }
    }
    
    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        if (cmbClase.getSelectedItem() != null) {
            clsItem clase = (clsItem) cmbClase.getSelectedItem();
            // ELIMINADO: Integer.parseInt (porque idclase es String)
            mtdListar(clase.getId());
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una clase para listar");
        }
    }//GEN-LAST:event_btnListarActionPerformed

