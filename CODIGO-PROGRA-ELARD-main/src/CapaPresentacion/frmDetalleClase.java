    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
        clsEDetalleClase objEDC = new clsEDetalleClase();
        clsNDetalleClase objNDC = new clsNDetalleClase();
        
        int filaS = tblDetalleClase.getSelectedRow();
        if (filaS != -1) {
            objEDC.setIdclase(tblDetalleClase.getValueAt(filaS, 0).toString());
            objEDC.setCodalumno(tblDetalleClase.getValueAt(filaS, 1).toString());

            if (objNDC.mtdEliminarDetalleClase(objEDC)) {
                JOptionPane.showMessageDialog(null, "Alumno retirado de la clase");
                mtdListar(objEDC.getIdclase());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione un alumno de la tabla");
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

