    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        clsEDocente objED = new clsEDocente();
        clsNDocente objND = new clsNDocente();
        
        objED.setCodigo(txtCodigo.getText());
        int confirm = JOptionPane.showConfirmDialog(null, "¿Eliminar docente?");

        if (confirm == JOptionPane.YES_OPTION) {
            if (objND.mtdEliminarDocente(objED)) {
                mtdListar();
                mtdLimpiar();
            }
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed

        clsEDocente objED = new clsEDocente();
        clsNDocente objND = new clsNDocente();
        
        objED.setCodigo(txtCodigo.getText());
        objED.setNombre(txtNombre.getText());

        if (objND.mtdModificarDocente(objED)) {
            JOptionPane.showMessageDialog(null, "Datos actualizados");
            mtdListar();
            mtdLimpiar();
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

        clsEDocente objED = new clsEDocente();
        clsNDocente objND = new clsNDocente();
        
        objED.setCodigo(txtCodigo.getText());
        objED.setNombre(txtNombre.getText());

        if (objND.mtdAgregarDocente(objED)) {
            JOptionPane.showMessageDialog(null, "Docente registrado");
            mtdListar();
            mtdLimpiar();
        }
        else{
            JOptionPane.showMessageDialog(null, 
            "Error al guardar.\nVerifique que todos los campos estén correctos:\n\n" +
            "• Código: solo números (máx. 6 dígitos)\n" +
            "• Nombre: mínimo 3 caracteres\n",
            "Error de validación", 
            JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_btnAgregarActionPerformed

