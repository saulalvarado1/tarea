    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        
        clsEDetalleMatricula objEDM = new clsEDetalleMatricula();
        clsNDetalleMatricula objNDM = new clsNDetalleMatricula();
        
        if (cmbMatricula.getSelectedItem() != null && cmbCurso.getSelectedItem() != null) {
            
            // 1. Obtener los items seleccionados
            
            clsItem matItem = (clsItem) cmbMatricula.getSelectedItem();
            clsItem cursoItem = (clsItem) cmbCurso.getSelectedItem();

            // 2. Pasar a la entidad (ID de matrícula convertido a int)
            objEDM.setIdmatricula(Integer.parseInt(matItem.getId()));
            objEDM.setCodcurso(cursoItem.getId());

            // 3. Ejecutar acción
            if (objNDM.mtdAgregarDetalle(objEDM)) {
                JOptionPane.showMessageDialog(null, "Curso agregado a la matrícula " + matItem.getId());
                mtdListar(objEDM.getIdmatricula());
            } else {
                JOptionPane.showMessageDialog(null, "Error al agregar curso");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una matrícula y un curso");
        }
        
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        
        clsEDetalleMatricula objEDM = new clsEDetalleMatricula();
        clsNDetalleMatricula objNDM = new clsNDetalleMatricula();
        
        int fila = tblDetalleMatricula.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "Seleccione un curso de la tabla para modificar");
            return;
        }

        // ID de la matrícula actual del combo
        clsItem matItem = (clsItem) cmbMatricula.getSelectedItem();
        // Nuevo curso seleccionado en el combo
        clsItem cursoNuevo = (clsItem) cmbCurso.getSelectedItem();
        // Curso antiguo (el que está en la tabla)
        String cursoAntiguo = tblDetalleMatricula.getValueAt(fila, 0).toString();

        // Primero eliminamos el antiguo y luego agregamos el nuevo
        objEDM.setIdmatricula(Integer.parseInt(matItem.getId()));
        objEDM.setCodcurso(cursoAntiguo);

        if (objNDM.mtdEliminarDetalle(objEDM)) {
            objEDM.setCodcurso(cursoNuevo.getId());
            if (objNDM.mtdAgregarDetalle(objEDM)) {
                JOptionPane.showMessageDialog(null, "Curso actualizado correctamente");
                mtdListar(objEDM.getIdmatricula());
            }
        }
        
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
        clsEDetalleMatricula objEDM = new clsEDetalleMatricula();
        clsNDetalleMatricula objNDM = new clsNDetalleMatricula();
        
        int fila = tblDetalleMatricula.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "Seleccione el curso que desea retirar de la lista");
            return;
        }

        clsItem matItem = (clsItem) cmbMatricula.getSelectedItem();
        String codCurso = tblDetalleMatricula.getValueAt(fila, 0).toString();

        objEDM.setIdmatricula(Integer.parseInt(matItem.getId()));
        objEDM.setCodcurso(codCurso);

        int resp = JOptionPane.showConfirmDialog(null, "¿Seguro que desea retirar este curso?");
        if (resp == JOptionPane.YES_OPTION) {
            if (objNDM.mtdEliminarDetalle(objEDM)) {
                mtdListar(objEDM.getIdmatricula());
                JOptionPane.showMessageDialog(null, "Curso retirado");
            }
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

